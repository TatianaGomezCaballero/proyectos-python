# mayuscula texto
texto = 'holi soy tati'
resultado = texto[2].upper()
print(resultado)

# primera letra en mayuscula
texto = 'holi soy tati'
resultado = texto.title()
print(resultado)


# primer aletra
texto = 'holi soy tati'
resultado = texto.capitalize()
print(resultado)


# invertir orden de letras

texto = 'holi soy tati'
resultado = texto[::-1]
print(resultado)



# join
a = 'holi'
b = 'tati'
c = ' '.join([a,b])
print(c)
